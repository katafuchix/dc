#!/usr/local/bin/perl
use Template;                    # テンプレートモジュールの読込
my $template = Template->new();  # テンプレートオブジェクトの作成
my $vars = {
    message        => "HelloWorld!!",           # スカラー
    };
$template->process(              # テンプレートに出力
      'ttk-include.tpl',$vars,$output
   ) or print Template->error();
print $output;
